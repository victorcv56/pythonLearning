print("Welcome to your calculator \n")

answer = input("What would you like to calculate today? \n")

if answer == '*':
    x = int(input())
    y = int(input())
    print(x * y)

elif answer == '/':
    x = int(input())
    y = int(input())
    print(x/y)

elif answer == '+':
    x = int(input())
    y = int(input())
    print(x + y)

elif answer == '-':
    x = int(input())
    y = int(input())
    print(x - y)

